package examjava1;
//표준 입력 예제
import java.util.Scanner;

public class Exam02 {
	public static void main(String[]args) {
		Scanner keyboard = new Scanner(System.in);
		//new 실체생성
		System.out.println("야 입력해봐");
		String data = keyboard.nextLine();
		//data를 변수라고 한다(자리)
		System.out.println(data);
		
		
	}
}
